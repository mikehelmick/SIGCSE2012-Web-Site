<h2>SIGCSE 2012: Teaching, Learning, and Collaborating<br/>
The 43rd ACM Technical Symposium on Computer Science Education</h2>

<p>February 29 - March 3, 2012, Raleigh, North Carolina, USA<br/>
<a href="http://www.sigcse.org/sigcse2012/">http://www.sigcse.org/sigcse2012/</a>

<p>SIGCSE 2012 continues our long tradition of bringing together colleagues from around the world to present papers, panels, posters, special sessions, and workshops, and to discuss computer science education in birds-of-a-feather sessions and informal settings. The SIGCSE Technical Symposium addresses problems common among educators working to develop, implement and/or evaluate computing programs, curricula, and courses. The symposium provides a forum for sharing new ideas for syllabi, laboratories, and other elements of teaching and pedagogy, at all levels of instruction.</p>

<p>Our three-sided conference theme, "Teaching, Learning, and Collaborating," commemorates North Carolina's renowned "Research Triangle" where SIGCSE 2012 will be held. Teaching, learning, and collaborating occur inside and outside of the classroom, among various combinations of students, academics, industry professionals, and others.</p>

<h3>PAPERS</h3>
<p>Papers describe a classroom experience, teaching technique, curricular initiative, or educational research project. Two versions of a submission are required: a full version having author names and affiliations and an anonymous version for use in reviewing. Papers will undergo a blind reviewing process and must not exceed six pages. Authors will have approximately 25 minutes for their presentations, including questions and answers.</p>

<h3>PANELS</h3>
<p>Panels present multiple perspectives on a specific topic. To allow each panelist sufficient time to present his or her perspective and still enable audience participation, a panel will normally have at most four panelists, including one moderator. Panel submissions should include a list of the panelists, their affiliations, and a description of the topic, with brief position statements from panelists. Proposals with more than four panelists must provide a statement connecting the extra panelist to the effectiveness of the panel and must convincingly show that each panelist will be able to speak, and the audience able to respond, within the session time. Panel abstracts must not exceed two pages. A panel session is approximately 75 minutes long.</p>

<h3>SPECIAL SESSIONS</h3>
<p>Special sessions are your opportunity to customize and experiment with the SIGCSE conference format. Possible special sessions include a seminar on a new topic, a committee report, or a forum on curriculum issues. More generally, they must be 75 minutes in length, held in standard conference spaces, and justifiably distinct from the panel, paper, and poster tracks. Within those constraints, the form is yours to design. Special session abstracts must not exceed two pages.</p>

<h3>WORKSHOPS</h3>
<p>Workshops provide introductory and advanced topics to help make participants more effective teachers and scholars. Workshop proposals (including abstract) must not exceed two pages. Submissions must specify equipment needs (e.g., participant laptops or projector required) and any limitation on the number of participants. Workshops are scheduled for a three-hour session and are offered separately from the technical track session times.</p>

<h3>BIRDS OF A FEATHER SESSIONS</h3>
<p>Birds of a Feather (BOF) sessions provide an environment for colleagues with similar interests to meet for informal discussions. A maximum one-page description (including abstract) is requested to describe the informal discussion topic. A/V equipment will not be provided for these sessions. Approximately 45 minutes are allocated to each BOF topic.</p>

<h3>POSTERS</h3>
<p>Posters describe computer science education materials or research, particularly works in progress. Proposals (including abstract) are limited to two pages. Poster demonstrations are scheduled to permit one-on-one discussion with conference attendees, typically during session breaks. Prepared handouts are encouraged in order to share your work.</p>

<h3>STUDENT RESEARCH COMPETITION</h3>
<p>Research from all areas of computer science is considered for awards in two categories of competition, graduate and undergraduate. All submissions must represent a student's individual research contribution and a student must be an ACM student member to qualify for awards and travel grants.</p>

