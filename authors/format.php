<h2><a name="requirements">Formatting Requirements for all<br/>Paper, Panel, and Special Session Submissions</a></h2>
</div></div> 

<p>The requirements listed in this section apply to all papers, panels, and special sessions:
<ul>
<li><b>Title:</b> The title should be centered, Arial or Helvetica, bold, 18 point, and Initial Letters Capitalized Like This.</li>
<li><b>Author information:</b> The author's name(s) should be centered using Arial or Helvetica 12 point. The affiliation and address should be Arial or Helvetica 10 point, and email should be Arial or Helvetica 12 point. Two or more authors may be listed side by side. If co-authors are at the same institution and share most information, you may use only one address. Please see the templates for examples. </li>
  <ul><li><b>SPECIAL NOTE FOR PANEL SUBMISSIONS:</b> Indicate which of the panelists is the moderator by placing the word "Moderator" in parentheses after her/his name.</li></ul>
<li><b>Paper size:</b> You should format your submission for 8.5 x11-inch paper.</li>
<li><b>Margins:</b> Top and bottom margins should be 1 inch, left and right margins should be 0.75 inch. This is for every page including the first.</li>
<li><b>Columns:</b> Text should be presented in two columns each 3.33 inches wide. There should be a 0.33 inch space between the columns. The two columns on the last page should be the same length approximately.</li>
<li><b>Section heads:</b> Section heads are flush left, Times Roman, bold, 12 point, ALL CAPITALS, and numbered starting at 1. There should be an additional 6 points of white space above the section head.</li>
<li><b>Subsection heads:</b> If your paper has subsections, they are flush left, Times Roman, bold, 12 point, and subnumbered (for example, 1.1). Initial letters of the subsection heading should be capitalized. There should be an additional 6 points of white space above the subsection head unless it immediately follows a section head. (Please see the templates for examples.)</li>
<li><b>Subsubsections:</b> If your paper has subsubsections, they are flush left, Times Roman, italics, 11 point, with initial letters capitalized, and subnumbered (for example, 1.1.2 or 1.2.3.4). There should be an additional 6 points of white space above the subsubsection heading, unless it immediately follows a subsection heading.</li>
<li><b>Text:</b> All text including abstract should be single spaced, full justification, Times Roman, and 9 point.</li>
<li><b>References:</b> Use the standard Communications of the ACM format for references. That is, references should be a numbered list at the end of the article, ordered alphabetically by first author, and referenced by numbers in square brackets, like this [1]. Use commas for multiple citations like this [3,4]. The reference section has a regular section head (i.e., numbered, ALL CAPITALS, Times Roman, bold, 12 point), and the references are 9 point Times Roman but with ragged right justification.</li>
<li><b>Copyright Space:</b> Leave 1.5 inches of blank space at the bottom of the left column of the first page for the copyright notice. Use a placeholder copyright notice with the number X-XXXXX-XX-X/XX/X for your submission. Please see the templates for examples.</li>
<li><b>Required Sections:</b> The following unnumbered sections are required at the beginning of the document in the following order:</li>
<li><b>Abstract:</b> The abstract should be a short description of the work described in the document. The title of the section ("ABSTRACT") should be formatted as a section head (i.e., flush left, Times Roman, bold, 12 point, ALL CAPITALS).</li>
<li><b>Categories and Subject Descriptors:</b> The ACM Computing Classification Scheme is available at <a href="http://www.acm.org/class/1998/">acm.org/class/1998/</a> Most submissions are likely to use category K.3.2 Computer and Information Science Education. The title of this section ("Categories and Subject Descriptors") should be formatted as a subsection head (i.e., flush left, Times Roman, bold, 12 point, Initial Letters Capitalized).</li>
<li><b>General Terms:</b> This section is limited to the following 16 terms (which are defined by the ACM Computing Classification Scheme): Algorithms, Management, Measurement, Documentation, Performance, Design, Economics, Reliability, Experimentation, Security, Human Factors, Standardization, Languages, Theory, Legal Aspects, Verification. The title of this section ("General Terms") should be formatted as a subsection head.</li>
<li><b>Keywords:</b> This section is your choice of words you would like your publication to be indexed by. The title of this section ("Keywords") should be formatted as a subsection head.</li>
<li><b>Other Requirements:</b> Do NOT use page numbers or headers/footers. Use a blank line between paragraphs.</li>
</ul>
</p>

<h3>ACM Web Reference Guidelines</h3>
<p>Elements (in most cases):
	<ol>
	<li>Author(s)</li>
	<li>Year of publication</li>
	<li>Title of 'document' - use initial caps on keywords and end in period.</li>
	<li>Name of Site in italics if given, and followed by period.</li>
	<li>Date accessed - Use 'Retrieved' followed by date as Month, DD, YYYY followed by 'from'</li>
	<li>Address - Given as '{http|ftp|telnet}://path'.</li>
	</ol>
	</p>
	<p>Note: a web address should never be given for a formally published document whose citation is complete or for which there is a DOI. Only give a web address for informal works or online-only works or resources that cannot otherwise be found by citation and/or DOI. Author Home page URLs or Institutional Repository URLs are not the way to cite formally published literature. If citing a formally published online-only publication, use the format for that genre and add elements 5 and 6 above.</p>
	<p><b>Examples:</b><br/>
	<code>
	H. Thornburg. 2001.  Introduction to Bayesian Statistics. Retrieved March 2, 2005 from http://ccrma.stanford.edu/~jos/bayes/bayes.html
	</code><br/>&nbsp;<br/>
	<code>
	Rafal Ablamowicz and Bertfried Fauser. 2007. CLIFFORD: a Maple 11 Package for Clifford Algebra Computations, version 11. Retrieved February 28, 2008 from http://math.tntech.edu/rafal/cliff11/index.html
	</code><br/>&nbsp;<br/>
	<code>
	Poker-Edge.Com. 2006. Stats and Analysis. Retrieved June 7, 2006 from http://www.poker-edge.com/stats.php	
	</code>

