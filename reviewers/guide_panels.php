<h1>Guidelines for Reviewers of Panels</h1>
<div class="SectionHeader"><div class="Full">
<h2>General Remarks</h2>
</div></div>   
<p>Your job as a reviewer of Panels is an important one. Through your reviews you have the opportunity to help the authors and to improve the quality of the symposium. Below are some general guidelines for writing good Panel reviews:</p>
<ul>
 <li>Your job is to write detailed reviews, even for excellent proposals. Tell the authors why you liked their proposal, so that they know what made it successful.</li>
 <li>If you believe that the proposal is poorly written or poorly thought-out, provide constructive criticism to help the authors.</li>
 <li>The best reviews clearly justify the reviewer's choice of rating.  The least valuable review gives a low score with no written comments.  This simply tells the authors that they have been unsuccessful, with no indication of how or why.  It is of no help to the members of the Program Committee, who are charged with making program decisions based on your reviews.</li>
 <li>Although SIGCSE requires all Panel proposals to be polished work, the authors will have a brief opportunity to improve their proposals before camera-ready copy is due.  Your detailed feedback may help improve a panel and, as a result, the conference.</li>
 <li><b>DISTINCTION BETWEEN PANELS AND SPECIAL SESSIONS:</b> Panels present multiple perspectives on a specific topic. Special sessions are an opportunity to customize and experiment with the SIGCSE conference format (e.g. a seminar on a new topic, a committee report, or a forum on curriculum issues) </li>     
</ul>

<div class="SectionHeader"><div class="Full">
<h2>More Detailed Suggestions</h2>
</div></div>  
<p>The questions on the Panel review form are quite general. The benefit of such a review form is that it allows you to write a wide range of comments that are appropriately tailored to the specific proposal.</p>
<p>At the same time, many reviewers appreciate specific suggestions of issues to consider as they read proposals and write reviews.  Where appropriate, please try to address the following in your review:</p>
<ul>
 <li>Is the panel topic suitable for the symposium?</li>
 <li>Do you expect that the level of interest in the panel would be high?</li>
 <li> Are the particular presenters appropriate?  Does their expertise match the panel topic? (Remember that Panel reviewing is not blind.)</li>
 <li> Does the proposal appropriately place the topic in the larger context of Computer Science education? Are the authors aware of a range of ideas on the topic? If appropriate, do the authors cite related work? (In evaluating this, please keep in mind that proposals are limited to two pages, so it is unreasonable to expect a detailed &quot;Related Work&quot; section or a long bibliography.)</li>
 <li>Is the structure of the panel presentation reasonable?
 <ul>
  <li>Is the topic a good one for a panel? Is there some other format that would be more appropriate (e.g., a special session, workshop or paper)?</li>
  <li>Will the speakers represent multiple views and offer distinct ideas?</li>
  <li> Will the speakers be able to present their views in the time allotted (75 minutes total), while leaving time for questions or audience participation (30 minutes suggested)?</li>
  <li> A typical panel consists of 4 participants including the moderator.  If there are more than 4, have they convinced you that everyone will be able to speak and that time will still be available for audience participation?</li>
 </ul></li>
 <li> Is the proposal well-written?  Is it clear and well-organized?  Are there any technical errors?</li>
 <li> Do you have any suggestions for the authors ...
  <ul>
   <li> ... to improve the proposal itself (either for publication in the proceedings or for submission to a future symposium)?</li>
   <li> ... to improve the quality of the presentation, if accepted?</li>
  </ul>
 </li>
</ul>

<p>Obviously this list is not exhaustive.  The Program Committee and authors will appreciate your views on other issues as well.</p>

