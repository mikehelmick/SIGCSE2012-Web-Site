<h1>Guidelines for Reviewers of Workshop Submissions</h1>

<div class="SectionHeader"><div class="Full">
<h2>General Remarks</h2>
</div></div>
<p>Our goal in choosing workshops is to provide a selection of workshops that will be attractive to attendees, well-organized, and effectively presented. Of course, it is also important that the presenters have appropriate expertise in the area of the workshop. With this in mind, answers to the following questions are most useful in workshop reviews:</p>
<ul>
 <li>Is this of broad interest to the community (i.e., will this get an audience)?</li>
 <li>Is this of narrow interest to the community (i.e., is it reasonably authoritative to experts in the specific area)?</li>
 <li>Is the proposal realistic given time, space, and resource constraints and the likely background of audience members? </li>
 <li>Is this likely to serve audiences well/will they come away satisfied?</li>
 <li>How novel/exciting is this?</li>
 <li>How can the abstract do a better job of communicating the target audience, content, and excitement of the workshop?</li>
</ul>
<p>Please include constructive criticism, even for a workshop proposal that you do not think should be included in the conference.  We hope the proposers will take your comments to heart and write better proposals for future workshops. Successful proposers have a chance to revise their abstract, and also, of course to plan their workshop with your suggestions in mind.</p>
<p>As with papers, the best reviews clearly justify the reviewer's choice of rating.</p>

<div class="SectionHeader"><div class="Full">
<h2>Examples of Helpful Reviews</h2>
</div></div>  
<p>Below are examples of comments that have been found in good, useful workshop reviews:</p>
<ul>
 <li><strong>Comments on relevance / audience attraction</strong>
  <ul>
   <li><em>There is a lot of interest in trying to teach YYY within the traditional CS program. This workshop has the potential to give attendees a good rubric for doing this as well as providing an infrastructure for implementing it.</em></li>
   <li><em>This Workshop Proposal addresses CS teacher education. It is therefore a main concern in SIGCSE, is fairly well described and the team shows experience on the subject.</em></li>
   <li><em>ZZZ is important in many areas and has become a very common way to present information. Making the topic available to this audience, I believe, is very appropriate.</em></li>
   <li><em>This is a well-formulated workshop proposal on a topic that is very likely to be appealing to most software engineering or CS capstone instructors. It is likely to have a significant level of participation and participants will most likely leave with a new set of tools for teaching SE.</em></li>
   <li><em>The topic is very narrow, covering use of a specific tool for a minor, optional part of a graphics course.</em></li>
   <li><em>ZZZ workshops have been well attended in the past; there's every reason to think there are plenty more educators who would benefit from hands-on practice (and who would be pretty disappointed if there were no ZZZ workshop at SIGCSE).</em></li>
   <li><em>The presenters are obviously well qualified. However, this topic has been around for a while now. My overall sense is that for SIGCSE participants there are now many suitable, alternate sources of the knowledge, skills, and resources that would be presented through this workshop, including the presenters' textbooks and online resources and guidance.</em></li>
   <li><em>This is a topic on which Web application teachers must keep up to date, and often do not have the time to do it. This workshop seems to be a good place for it.</em></li>
 </ul></li>
 <li><strong>Comments on proposal quality / presenter qualifications</strong>
 <ul>
  <li><em>With just a 1-page summary, the workshop outcomes are not clearly defined.  What will the workshop participant know at the end of the workshop? How might a participant use the skills learned in the workshop in their own teaching and/or research?</em></li>
  <li><em>I would like to have read a more detailed table of contents; specifically a systematic table of the methods planned to be included in the workshop.</em></li>
  <li><em>The materials are well tested and the workshop has been tried out. The presenter is the developer of the materials and has experience with similar workshops, so he is well qualified.  I like that the goal of the workshop is to get participants to the point where they can design their own labs on this material, rather than just present existing exercises (though those are very valuable!).</em></li>
  <li><em>The workshop proposal is very thorough and specific about what it will cover and what it provides attendees.  It is also good that the presenters have a contingency plan in case equipment issues arise. This proposal is very well thought out.</em></li>
  <li><em>The problem here is that there is little detail. There is no syllabus, no detail about methods or software.  The take home materials are scant for a software development workshop; I would think a CD with materials on it would be more appropriate than a "detailed handout".</em></li>
  <li><em>Workshop organizers are experts in this area. They clearly state what the goals of the workshop are and give a taste of the type of strategies that will be discussed in the workshop. Attendees will receive high quality materials they can take back to their institutions to help convey the message of the workshop and seek backing for their plans from administration. The fact that the workshop organizers can provide research basis for the suggested strategies is very appealing.</em></li>
 </ul></li>
 <li><strong>Workshop Presentation Comments</strong>
 <ul>
  <li><em>Run the workshop through some of the exercises done by students.  Do you have measureable outcomes to point out the success of this approach over (1) standard lecture and/or (2) work of others?</em></li>
  <li><em>In addition to covering example activities, emphasize the classroom management aspect. This is probably a worry to instructors who are considering a drastic change to their teaching style.</em></li>
  <li><em>While the proposal is directed to attendees who are new to ZZZ, my expectation is that teachers with some experience will also attend.  The presenters may need to be prepared for different levels of experience and interest.</em></li>
 </ul>
 </li>
</ul>

