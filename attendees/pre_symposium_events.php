<h2><a name="oss">Teaching Open Source Symposium</a></h2>
</div></div>
<ul>
 <li><img src="/sigcse2012/images/clock.png" align="absmiddle" />Wednesday, February 29, 2012, 9:00am to 5:00pm. </li>
 <li>Cost:  $50</li>
</ul>
<p>The Teaching Open Source Symposium is a gathering spot for those interested in student participation in open, informal communities as a way to further the learning of computing. While 
most of us do this by assigning classwork that involves students in open source software projects such as Mozilla Firefox, various Linux distributions, or humanitarian free and open source software (HFOSS), we also cover open hardware and content projects (as in Wikipedia), hackerspaces, language and other technical meetup groups, and communication processes that promote realtime transparency such as the use of wikis and blogging. This event unites newcomers with members from the Teaching Open Source community (<a href="http://teachingopensource.org">http://teachingopensource.org</a>), an emergent (3 year old) group working on scaffolding to bridge the cultural differences between academic and FOSS communities of practice. Join us to share questions, challenges, and triumphs of incorporating FOSS participation into existing and new curricula as well support resources for doing so. Alumni and current members of the POSSE (<a href="http://communityleadershipteam.org/posse">Professors' Open Source Summer Experience</a>) will attend in mentorship roles, as we discuss topics in an unconference-style environment, a format specifically designed to invoke participation, teaching, and learning by all attendees regardless of prior topic experience.
</p>
<p>For additional information please go to <a href="http://teachingopensource.org/index.php/SIGCSE_2012/TOS_symposium">http://teachingopensource.org/index.php/SIGCSE_2012/TOS_symposium</a>
or you can contact Mel Chua (<script>write_email("mel", "purdue.edu");</script> or Heidi Ellis (<script>write_email("ellis", "wne.edu");</script>).

