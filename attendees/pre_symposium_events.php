<p>In support of ACM's Commitment to Ethical Professionalism this event is designed to help faculty provide students with tools to better understand and resolve the ethical challenges. It will specifically address the needs of those who are teaching or about to teach Computer ethics.  It will emphasize active learning with small group activities engaging the participants in interactive exercises to introduce and enhance their computer ethics teaching skills; there will be exercises in developing course syllabi, designing and evaluating class materials, ethical scenarios. We will also make available to the participants material which can be used to engage students in the study of applied ethics. 
More information at <a href="https://edocs.uis.edu/kmill2/www/sigcse2012computerEthics.html">https://edocs.uis.edu/kmill2/www/sigcse2012computerEthics.html</a>
