<h2><a name="alice2">Programming with Alice Workshop (Alice 2.2)</a></h2>
</div></div>
<ul>
 <li><img src="/sigcse2012/images/clock.png" align="absmiddle" />Wednesday, February 29, 2012, 9:00am - 5:00pm.</li>
